;	LIST P=16F628A
	INCLUDE <p16f628a.inc>
	__CONFIG _CP_OFF & _WDT_OFF & _PWRTE_OFF & _BODEN_OFF & _MCLRE_ON & _INTRC_OSC_NOCLKOUT & _LVP_OFF



	ORG 0X0000

INICIO:
	MOVLW B'00000111'
	MOVWF CMCON

	CLRF	INTCON
	
	BANKSEL TRISB
	MOVLW B'00000000'
	MOVWF   TRISB
	BANKSEL PORTB

	CLRF CCP1CON
	CLRF TMR2

	MOVLW 0X7F
	MOVWF PR2

	MOVLW 0x1F
	MOVWF CCPR1L

	MOVLW 0x2C
	MOVWF CCP1CON
	
	CLRF T2CON
	BSF  T2CON,TMR2ON
			

LOOP:

	GOTO LOOP

	END

